package models;

/**
 *
 * @author Dat
 */
public class Account {
    private String user, pass;

    public Account(String user, String pass) {
        this.user = user;
        this.pass = pass;
    }

    public Account() {
    }

    public String getUser() {
        return user;
    }

    public String getPass() {
        return pass;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
    
}
